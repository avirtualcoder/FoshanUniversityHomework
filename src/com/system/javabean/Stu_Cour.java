package com.system.javabean;

public class Stu_Cour {
	private String Sno;
	private String Cno;
	private float grade;

	public void setSno(String Sno) {
		this.Sno = Sno;
	}

	public void setCno(String Cno) {
		this.Cno = Cno;
	}

	public void setGrade(float grade) {
		this.grade = grade;
	}

	public String getSno() {
		return Sno;
	}

	public String getCno() {
		return Cno;
	}

	public float getGrade() {
		return grade;
	}
}
